# This program uses input to find the sum, difference, product, quotient, remainder, integer quotient, and exponent

num_1 = int(input("Enter the first number: "))
num_2 = int(input("Enter the second number: "))

print("Sum: ", num_1 + num_2)
print("Difference: ", num_1 - num_2)
print("Product: ", num_1 * num_2)
print("Quotient: ", num_1 / num_2)
print("Remainder: ", num_1 % num_2)
print("Integer Quotient: ", num_1 // num_2)
print("Exponent: ", num_1 ** num_2)
