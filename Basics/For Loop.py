# This program has for loop

# Print numbers from 1 through 10
for n in range(1, 11):
    print("1st loop: ", n)

# Print even numbers from 2 through 20
for n in range(2, 21, 2):
    print("2nd loop: ", n)

# Print decreasing numbers from 5 to -10
for n in range(5, -11, -1):
    print("3rd loop: ", n)